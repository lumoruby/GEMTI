function calculateResult() {
  // ì„±í–¥ ê²°ì •
  const traits = [];
  traits.push(answers.E >= answers.I ? 'E' : 'I');
  traits.push(answers.S >= answers.N ? 'S' : 'N');
  traits.push(answers.T >= answers.F ? 'T' : 'F');
  traits.push(answers.J >= answers.P ? 'J' : 'P');

  // ê°€ì¥ ì í•©í•œ ë³´ì„ ì°¾ê¸°
  let bestGem = gems[0];
  let bestScore = 0;

  gems.forEach(gem => {
    let score = 0;
    gem.traits.forEach(trait => {
      if (traits.includes(trait)) {
        score++;
      }
    });

    // ì •í™•íˆ ë§¤ì¹­ë˜ëŠ” íŠ¹ì„± ìˆ˜ì— ê°€ì¤‘ì¹˜ ë¶€ì—¬
    if (score > bestScore) {
      bestScore = score;
      bestGem = gem;
    } else if (score === bestScore) {
      // ì ìˆ˜ê°€ ê°™ìœ¼ë©´ ëœë¤í•˜ê²Œ ì„ íƒ
      if (Math.random() > 0.5) {
        bestGem = gem;
      }
    }
  });

  displayResult(bestGem);
}

